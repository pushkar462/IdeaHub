/* ---------- CREATE POST ---------- */
export const createPost = async (req: Request, res: Response) => {
  const { title, description, type, section, isUseCase, linkedEntityType, linkedEntityId, campaignId } = req.body;
  const authorId = req.user!.id;

  // Handbook B8: only allow tagging to an ACTIVE campaign. Silently drop otherwise.
  let resolvedCampaignId: number | undefined;
  if (campaignId) {
    const cid = Number(campaignId);
    if (Number.isInteger(cid) && cid > 0) {
      const c = await prisma.campaign.findUnique({ where: { id: cid }, select: { status: true } });
      if (c?.status === 'ACTIVE') resolvedCampaignId = cid;
    }
  }

  let attachmentData = null;
  if (req.file) {
    try {
      const result = await storageService.upload(
        req.file.buffer,
        req.file.originalname,
        req.file.mimetype,
        'POST_ATTACHMENT',
        false
      );
      attachmentData = {
        url: result.url,
        filename: req.file.originalname,
      };
    } catch (storageErr: any) {
      console.error('Storage upload failed:', storageErr);
      throw new AppError(
        storageErr?.message || 'Upload failed. Please try again.',
        StatusCodes.BAD_REQUEST,
        'UPLOAD_FAILED'
      );
    }
  }

  // postNumber is generated by the `generate_loop_post_number` Postgres trigger
  // (migration 20260708120000_state_machine_triggers). Do not compute it here.

  const ownerId = await getSectionOwner(section as Section);

  let post;
  try {
    post = await prisma.post.create({
      data: {
        title,
        description,
        type,
        section,
        isUseCase: isUseCase || false,
        linkedEntityType,
        linkedEntityId,
        campaignId: resolvedCampaignId,
        ownerId,
        authorId,
        status: Status.OPEN,
        attachments: attachmentData ? {
          create: [{
            url: attachmentData.url,
            filename: attachmentData.filename,
            mimeType: req.file!.mimetype,
          }]
        } : undefined
      },
      include: { author: { select: { id: true, name: true, role: true, avatarUrl: true } }, attachments: true },
    });
  } catch (error: any) {
    console.error('[createPost] Prisma create failed:', error);
    throw error;
  }

  // Fire-and-forget side effects — never let these crash the main response
  Promise.all([
    mentionService.processMentions({ text: description, authorId, postId: post.id }).catch(e =>
      console.warn('processMentions failed:', e)
    ),
    ownerId && ownerId !== authorId
      ? notificationService.createNotification({
          userId: ownerId,
          type: 'ASSIGNMENT', // Using ASSIGNMENT type for ownership alert for now
          actorId: authorId,
          postId: post.id,
        }, undefined).catch(e => console.warn('owner notification failed:', e))
      : Promise.resolve(),
    // Handbook P4 · index for semantic search. No-op when embeddings disabled.
    semanticSearchService.indexPostAsync(post.id, `${post.title}\n\n${post.description}`),
  ]).catch(() => {});

  return successResponse(res, 'Post created successfully', post, {}, StatusCodes.CREATED);
};

