/* ---------- REACT TO POST ---------- */
export const reactToPost = async (req: Request, res: Response) => {
  const postId = Number(req.params.id);
  const { emoji } = req.body;
  const userId = req.user!.id;

  const existing = await prisma.reaction.findUnique({
    where: { userId_postId_emoji: { userId, postId, emoji } },
  });

  if (existing) {
    await prisma.reaction.delete({ where: { id: existing.id } });
    return successResponse(res, 'Reaction removed', { removed: true });
  } else {
    const reaction = await prisma.reaction.create({
      data: { userId, postId, emoji },
    });
    return successResponse(res, 'Reaction added', reaction, {}, StatusCodes.CREATED);
  }
};

/* ---------- VOTE ON POST (toggle) ---------- */
// Handbook C1 · one vote per user per post. Composite PK on `Vote` enforces
// it at the DB level; this endpoint just toggles.
export const votePost = async (req: Request, res: Response) => {
  const postId = Number(req.params.id);
  if (!Number.isInteger(postId) || postId <= 0) {
    throw new AppError('Invalid post ID', StatusCodes.BAD_REQUEST, 'INVALID_ID');
  }
  const userId = req.user!.id;

  const existing = await prisma.vote.findUnique({
    where: { postId_userId: { postId, userId } },
  });

  if (existing) {
    await prisma.vote.delete({ where: { postId_userId: { postId, userId } } });
    const voteCount = await prisma.vote.count({ where: { postId } });
    return successResponse(res, 'Vote removed', { hasVoted: false, voteCount });
  }

  await prisma.vote.create({ data: { postId, userId } });
  const voteCount = await prisma.vote.count({ where: { postId } });
  return successResponse(res, 'Vote recorded', { hasVoted: true, voteCount }, {}, StatusCodes.CREATED);
};

/* ---------- DELETE POST ---------- */
export const deletePost = async (req: Request, res: Response) => {
  const id = Number(req.params.id);
  const post = await prisma.post.findUnique({
    where: { id },
    include: { attachments: true },
  });
  
  if (!post) {
    throw new AppError('Post not found', StatusCodes.NOT_FOUND, 'POST_NOT_FOUND');
  }
  
  if (post.authorId !== req.user!.id && req.user!.role !== 'FOUNDER' && req.user!.role !== 'ADMIN') {
    throw new AppError('Forbidden. You cannot delete this post.', StatusCodes.FORBIDDEN, 'FORBIDDEN');
  }

  // Clean up attachment files from storage
  for (const attachment of post.attachments) {
    const key = storageService.extractKey(attachment.url);
    await storageService.delete(key).catch((e) =>
      console.warn(`Failed to delete attachment file ${key}:`, e)
    );
  }
  
  await prisma.post.delete({ where: { id } });
  return successResponse(res, 'Post deleted successfully');
};

/* ---------- GET STATS ---------- */
export const getStats = async (req: Request, res: Response) => {
  const userId = req.user!.id;

  const [totalActive, myActiveTasks, needReview, completed] = await Promise.all([
    // Total active posts across the entire platform
    prisma.post.count({ where: { status: { not: Status.RESOLVED } } }),
    // Tasks assigned to me (owned) that are not RESOLVED
    prisma.post.count({ where: { ownerId: userId, status: { not: Status.RESOLVED } } }),
    // Posts waiting for review (we can use OPEN as an approximation for now)
    prisma.post.count({ where: { status: Status.OPEN } }),
    // Total posts I have authored or been assigned to that are RESOLVED
    prisma.post.count({
      where: {
        status: Status.RESOLVED,
        OR: [{ authorId: userId }, { ownerId: userId }],
      },
    }),
  ]);

  return successResponse(res, 'Stats retrieved', {
    totalActive,
    myActiveTasks,
    needReview,
    completed,
  });
};

/* ---------- GET SLA HEALTH ---------- */
// Admin/Founder only. Surfaces the "black-hole rate" — the share of currently-Open posts
// past the 48h acknowledge window with no acknowledgedAt — plus slaStatus counts and
// average time-to-acknowledge on posts that were acknowledged.
export const getSlaHealth = async (req: Request, res: Response) => {
  const role = req.user!.role;
  if (role !== 'ADMIN' && role !== 'FOUNDER') {
    throw new AppError('SLA dashboard is restricted to Admin/Founder', StatusCodes.FORBIDDEN, 'FORBIDDEN');
  }

  const ACK_WINDOW_MS = 48 * 60 * 60 * 1000;
  const windowCutoff = new Date(Date.now() - ACK_WINDOW_MS);

  const [openTotal, blackHoleCount, statusHealthy, statusAtRisk, statusBreached, ackSample] = await Promise.all([
    prisma.post.count({ where: { status: Status.OPEN } }),
    prisma.post.count({
      where: { status: Status.OPEN, acknowledgedAt: null, createdAt: { lt: windowCutoff } },
    }),
    prisma.workflowMetrics.count({ where: { slaStatus: 'HEALTHY' } }),
    prisma.workflowMetrics.count({ where: { slaStatus: 'AT_RISK' } }),
    prisma.workflowMetrics.count({ where: { slaStatus: 'BREACHED' } }),
    prisma.post.findMany({
      where: { acknowledgedAt: { not: null } },
      select: { createdAt: true, acknowledgedAt: true },
      orderBy: { acknowledgedAt: 'desc' },
      take: 500,
    }),
  ]);

  const ackDeltas = ackSample
    .map((p) => (p.acknowledgedAt ? p.acknowledgedAt.getTime() - p.createdAt.getTime() : null))
    .filter((n): n is number => n !== null && n >= 0);
  const avgTimeToAcknowledgeMs = ackDeltas.length
    ? Math.round(ackDeltas.reduce((s, n) => s + n, 0) / ackDeltas.length)
    : null;

  const blackHoleRate = openTotal > 0 ? blackHoleCount / openTotal : 0;

  return successResponse(res, 'SLA health retrieved', {
    openTotal,
    blackHoleCount,
    blackHoleRate,
    slaStatusCounts: {
      HEALTHY: statusHealthy,
      AT_RISK: statusAtRisk,
      BREACHED: statusBreached,
    },
    avgTimeToAcknowledgeMs,
    sampleSize: ackDeltas.length,
  });
};

/* ---------- GET LOOP HEALTH (Admin dashboard) ---------- */
// Handbook C4 · Admin dashboard. Extends /sla-health with time-to-resolve,
// need-response count, a 30d black-hole rate, and a per-section breakdown.
export const getLoopHealth = async (req: Request, res: Response) => {
  const role = req.user!.role;
  if (role !== 'ADMIN' && role !== 'FOUNDER') {
    throw new AppError('Loop health dashboard is restricted to Admin/Founder', StatusCodes.FORBIDDEN, 'FORBIDDEN');
  }

  const ACK_WINDOW_MS = 48 * 60 * 60 * 1000;
  const now = Date.now();
  const ackCutoff  = new Date(now - ACK_WINDOW_MS);
  const thirtyDaysAgo = new Date(now - 30 * 24 * 60 * 60 * 1000);

  const [
    openTotal,
    needResponseCount,
    blackHoleCount,
    blackHole30d,
    open30d,
    statusHealthy,
    statusAtRisk,
    statusBreached,
    ackSample,
    resolvedSample,
    sectionCounts,
    sectionOpenCounts,
    sectionBreachedCounts,
  ] = await Promise.all([
    prisma.post.count({ where: { status: Status.OPEN } }),
    prisma.post.count({ where: { status: Status.OPEN, acknowledgedAt: null } }),
    prisma.post.count({ where: { status: Status.OPEN, acknowledgedAt: null, createdAt: { lt: ackCutoff } } }),
    // 30-day rolling: breached (as recorded by cron) among posts opened in the window
    prisma.post.count({
      where: { createdAt: { gte: thirtyDaysAgo }, workflowMetrics: { slaStatus: 'BREACHED' } },
    }),
    prisma.post.count({ where: { createdAt: { gte: thirtyDaysAgo } } }),
    prisma.workflowMetrics.count({ where: { slaStatus: 'HEALTHY' } }),
    prisma.workflowMetrics.count({ where: { slaStatus: 'AT_RISK' } }),
    prisma.workflowMetrics.count({ where: { slaStatus: 'BREACHED' } }),
    prisma.post.findMany({
      where: { acknowledgedAt: { not: null } },
      select: { createdAt: true, acknowledgedAt: true },
      orderBy: { acknowledgedAt: 'desc' },
      take: 500,
    }),
    prisma.workflowMetrics.findMany({
      where: { completedAt: { not: null }, updatedAt: { gte: thirtyDaysAgo } },
      select: { createdAt: true, completedAt: true, post: { select: { createdAt: true } } },
      orderBy: { completedAt: 'desc' },
      take: 500,
    }),
    prisma.post.groupBy({ by: ['section'], _count: { _all: true } }),
    prisma.post.groupBy({ by: ['section'], where: { status: Status.OPEN }, _count: { _all: true } }),
    prisma.post.groupBy({
      by: ['section'],
      where: { workflowMetrics: { slaStatus: 'BREACHED' } },
      _count: { _all: true },
    }),
  ]);

  const ackDeltas = ackSample
    .map((p) => (p.acknowledgedAt ? p.acknowledgedAt.getTime() - p.createdAt.getTime() : null))
    .filter((n): n is number => n !== null && n >= 0);
  const avgTimeToAcknowledgeMs = ackDeltas.length
    ? Math.round(ackDeltas.reduce((s, n) => s + n, 0) / ackDeltas.length)
    : null;

  const resolveDeltas = resolvedSample
    .map((m) =>
      m.completedAt && m.post ? m.completedAt.getTime() - m.post.createdAt.getTime() : null,
    )
    .filter((n): n is number => n !== null && n >= 0);
  const avgTimeToResolveMs = resolveDeltas.length
    ? Math.round(resolveDeltas.reduce((s, n) => s + n, 0) / resolveDeltas.length)
    : null;

  const blackHoleRate     = openTotal > 0 ? blackHoleCount / openTotal : 0;
  const blackHoleRate30d  = open30d > 0 ? blackHole30d / open30d : 0;

  const openBySection = new Map(sectionOpenCounts.map((r) => [r.section, r._count._all]));
  const breachedBySection = new Map(sectionBreachedCounts.map((r) => [r.section, r._count._all]));
  const perSection = sectionCounts
    .map((r) => ({
      section: r.section,
      total: r._count._all,
      open: openBySection.get(r.section) ?? 0,
      breached: breachedBySection.get(r.section) ?? 0,
    }))
    .sort((a, b) => b.open - a.open);

  return successResponse(res, 'Loop health retrieved', {
    openTotal,
    needResponseCount,
    blackHoleCount,
    blackHoleRate,
    blackHoleRate30d,
    slaStatusCounts: {
      HEALTHY: statusHealthy,
      AT_RISK: statusAtRisk,
      BREACHED: statusBreached,
    },
    avgTimeToAcknowledgeMs,
    avgTimeToResolveMs,
    ackSampleSize: ackDeltas.length,
    resolveSampleSize: resolveDeltas.length,
    perSection,
    generatedAt: new Date().toISOString(),
  });
};
